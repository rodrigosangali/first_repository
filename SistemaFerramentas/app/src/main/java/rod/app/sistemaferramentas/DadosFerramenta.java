public class DadosFerramenta {

    String nome_ferramenta;
    String fabricante;
    String referencia;

    public void setNomeFerramenta(String n)     {         nome_ferramenta = n;     }

    public String getNomeFerramenta()     {         return nome_ferramenta;     }

    public void setFabricante(String f)     {         fabricante = f;     }

    public String getFabricante()     {         return fabricante;     }

    public void setReferencia(String ref)     {         referencia = ref;     }

    public String getReferencia()     {         return referencia;     }

}

